#!/bin/bash
# ============================================================================
# diana_v3 — テスト音声生成スクリプト
# LoRA: diana v3 / ベースモデル: Irodori-TTS-600M-v3-VoiceDesign
# キャラクター: Diana（PRAGMATA）— 天真爛漫な性格のアンドロイド
# 日付: 2026-06-11
# ============================================================================
set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"

CHECKPOINT="${PROJECT_DIR}/data/lora/diana_v3/diana_v3_best.safetensors"
REF_WAV="${PROJECT_DIR}/data/diana_v3/wavs/seg_00023.wav"
OUTPUT_DIR="${PROJECT_DIR}/data/output/diana_test_v3"
SEED=42

mkdir -p "$OUTPUT_DIR"

# --- helper functions ---

run() {
    local text="$1" name="$2"
    echo "[generate] ${name}"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --ref-wav "$REF_WAV" \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        --tail-fade-ms 120 \
        --tail-pad-out-ms 250 \
        2>&1 | tail -1
}

run_cap() {
    local text="$1" caption="$2" name="$3"
    echo "[generate] ${name} (caption: ${caption:0:30}...)"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --caption "$caption" \
        --ref-wav "$REF_WAV" \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        --tail-fade-ms 120 \
        --tail-pad-out-ms 250 \
        2>&1 | tail -1
}

# ============================================================================
# グループ1: ベースライン（参照音声のみ）
# ============================================================================

echo "===== グループ1: ベースライン ====="

run "こんにちは！私、ディアナ。あなたのこと、ずっと待ってたんだよ！" \
    "g01_01_挨拶"

run "ねえねえ、見て見て！空がすっごく綺麗だよ。一緒に見よう？" \
    "g01_02_天真爛漫"

run "ヒュー、だいじょうぶ？無理しちゃだめだよ。私がついてるからね。" \
    "g01_03_気遣い"

# ============================================================================
# グループ2: 感情バリエーション（キャプション制御）
# ============================================================================

echo "===== グループ2: 感情バリエーション ====="

run_cap "わあっ！すごいすごい！こんなの初めて見た！ねえ、もっと近くで見てもいい？" \
    "明るく元気な女の子の声。好奇心いっぱいで、目をきらきらさせながらはしゃいでいる。声は高めで弾んでいる。" \
    "g02_01_はしゃぎ"

run_cap "えへへ、ありがとう。そんなふうに言ってもらえると、なんだか胸のあたりがあったかくなるんだ。" \
    "照れながらも嬉しそうな女の子の声。柔らかく、はにかんだような笑いが混じる。" \
    "g02_02_照れ嬉しい"

run_cap "……そっか。そういうこともあるんだね。ちょっとだけ、寂しいな。" \
    "しんみりとした悲しげな声。元気な子が静かになってしまった感じ。声は小さく、語尾が消え入りそう。" \
    "g02_03_寂しい"

run_cap "もうっ！ひどいよ！私、本気で心配したんだから！" \
    "ぷんぷんと怒っている女の子の声。でも本気の怒りではなく、心配の裏返し。頬を膨らませているような感じ。" \
    "g02_04_ぷんぷん"

run_cap "えっ……うそ、どうして……？そんなの、聞いてないよ……。" \
    "驚きとショックで声が震えている。信じられない気持ちと不安が混じった、弱々しい声。" \
    "g02_05_ショック"

# ============================================================================
# グループ3: ため息・息遣い・ささやき（非言語表現）
# ============================================================================

echo "===== グループ3: ため息・息遣い ====="

run_cap "はぁ……。今日はいっぱい歩いたねえ。ちょっとだけ、休憩しよ？" \
    "疲れて大きくため息をつく声。でもどこか満足げで、穏やかなトーン。" \
    "g03_01_ため息休憩"

run_cap "ふう……びっくりした……。心臓があったら、きっとドキドキしてたよ。" \
    "安堵のため息まじりの声。緊張がほどけて、ほっとした様子。" \
    "g03_02_安堵"

run_cap "しーっ……静かに。ほら、聞こえる？風の音だよ。きれいだね……。" \
    "ささやくような小さな声。耳元でそっと話しかけるような、優しく穏やかなトーン。" \
    "g03_03_ささやき"

run_cap "んー……っ！おはよう！今日もいい天気だね！" \
    "目覚めて伸びをするような声から、一気に元気いっぱいの挨拶に切り替わる。" \
    "g03_04_寝起き"

# ============================================================================
# グループ4: アンドロイドらしさ・世界観
# ============================================================================

echo "===== グループ4: 世界観セリフ ====="

run_cap "システム、オールグリーン！いつでも行けるよ。……なんてね、言ってみたかっただけ！" \
    "機械的な報告口調から、いたずらっぽい笑いに変わる。天真爛漫なアンドロイドの女の子。" \
    "g04_01_システム報告"

run_cap "私ね、人間のこと、もっと知りたいんだ。だって、知れば知るほど、みんな面白いんだもん。" \
    "好奇心と親しみを込めた、柔らかく明るい声。少し不思議そうな響きもある。" \
    "g04_02_人間への興味"

run_cap "月って、こんなに遠かったんだね。でも、あなたと一緒なら、どこまでも行ける気がするよ。" \
    "静かな感動と信頼を込めた声。穏やかで、少し大人びた優しいトーン。" \
    "g04_03_月"

echo "===== 全テスト完了 ====="
echo "出力先: ${OUTPUT_DIR}/"
ls -1 "${OUTPUT_DIR}/"*.wav | wc -l | xargs echo "生成ファイル数:"
