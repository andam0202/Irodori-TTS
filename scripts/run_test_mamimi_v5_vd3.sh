#!/bin/bash
# ============================================================================
# mamimi_v5_vd3 — テスト音声生成スクリプト
# LoRA: mamimi v5 / ベースモデル: Irodori-TTS-600M-v3-VoiceDesign
# 日付: 2026-06-03
# ============================================================================
set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"

CHECKPOINT="${PROJECT_DIR}/data/lora/mamimi_v5_forV3/mamimi_v5_forV3_best.safetensors"
REF_WAV="${PROJECT_DIR}/data/mamimi/wavs/seg_00000.wav"
OUTPUT_DIR="${PROJECT_DIR}/data/output/mamimi_test_v5_vd3"
SEED=42

mkdir -p "$OUTPUT_DIR"

# --- helper functions ---

run() {
    local text="$1" name="$2"
    echo "[generate] ${name}"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --ref-wav "$REF_WAV" \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        2>&1 | tail -1
}

run_cap() {
    local text="$1" caption="$2" name="$3"
    echo "[generate] ${name} (caption: ${caption:0:30}...)"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --caption "$caption" \
        --ref-wav "$REF_WAV" \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        2>&1 | tail -1
}

run_noref() {
    local text="$1" caption="$2" name="$3"
    echo "[generate] ${name} (no-ref, caption: ${caption:0:30}...)"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --caption "$caption" \
        --no-ref \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        2>&1 | tail -1
}

# ============================================================================
# グループ1: キャプションなし ベースライン（絵文字の効果）
# ============================================================================

TEXT_BASE="プロデューサー、今日は私のために時間を作ってくれてありがとう。大事な話があるんだ。"
TEXT_EMOJI="プロデューサー…😢今日は私のために時間を作ってくれてありがとう。😤でも…大事な話があるんだ🤭聞いてくれる？"

echo "===== グループ1: ベースライン ====="

# 1-1: 絵文字なし、キャプションなし
run "$TEXT_BASE" "g01_01_絵文字なし_キャプションなし"

# 1-2: 絵文字あり、キャプションなし
run "$TEXT_EMOJI" "g01_02_絵文字あり_キャプションなし"

# ============================================================================
# グループ2: 同じテキスト＋異なるキャプション
# ============================================================================

echo "===== グループ2: キャプション効果比較 ====="

# 2-1: 内気・緊張
run_cap "$TEXT_EMOJI" \
    "少し内気な10代の女の子。緊張しているが、勇気を振り絞って大切なことを伝えようとしている。声は小さめで、途切れ途切れになりがち。" \
    "g02_01_内気緊張"

# 2-2: 明るく元気
run_cap "$TEXT_EMOJI" \
    "明るく元気な10代の女の子。親しい相手に楽しそうに、少し早口で話している。声は高めでハキハキとしている。" \
    "g02_02_明るく元気"

# 2-3: 怒り・感情的
run_cap "$TEXT_EMOJI" \
    "激しい怒りを感じている女の子。相手を責め立てるような強い口調で、感情的に声を荒らげている。" \
    "g02_03_怒り感情的"

# 2-4: 落ち着き・冷静
run_cap "$TEXT_EMOJI" \
    "落ち着いた冷静な女の子。感情を抑えて、静かに淡々と話している。声は低めで穏やか。" \
    "g02_04_落ち着き冷静"

# 2-5: 泣き声・悲痛
run_cap "$TEXT_EMOJI" \
    "深く傷つき、今にも泣き出しそうな女の子。声が震えており、悲痛なトーンで弱々しく話す。涙声が混じっている。" \
    "g02_05_泣き声悲痛"

# 2-6: 呆れ・冷笑
run_cap "$TEXT_EMOJI" \
    "完全に呆れ返っている女の子。感情の起伏が乏しく、冷たいトーンで静かに突き放すように話す。" \
    "g02_06_呆れ冷笑"

# ============================================================================
# グループ3: 異なるテキスト＋絵文字＋キャプション
# ============================================================================

echo "===== グループ3: 表現力テスト ====="

# 3-1: 笑い + ため息
run_cap "あははっ🤭、それ本当に言ってるの？…😮‍💨まぁ、君らしいけどね。" \
    "少し内気な10代の女の子。親しい相手に呆れながらも楽しそうに話している。" \
    "g03_01_笑いため息"

# 3-2: 咳き込み + 泣き
run_cap "ゲホッ🤧…ごめん、少し休ませて😭今日はもう無理みたい。" \
    "体調が悪く苦しそうな女の子。息も絶え絶えに、申し訳なさそうに弱々しい声で話している。" \
    "g03_02_咳き込み泣き"

# 3-3: 普通の会話（絵文字なし・キャプションあり）
run_cap "別に普通ですよ。そんなに大したことじゃないし、気にしないでください。" \
    "少し内気な10代の女の子。ぶっきらぼうに話しているが、相手を気遣う気持ちが少し見える。" \
    "g03_03_普通会話_キャプションあり"

# 3-4: 驚き
run_cap "えっ！？😮本当に！？😲うそでしょ！？" \
    "驚いている女の子。予想外のことを聞いて目を丸くしている。声は高めでテンションが上がっている。" \
    "g03_04_驚き"

# 3-5: 困り
run_cap "うーん😤…どうしようかな。悩みすぎてもよくわかんなくなっちゃった😅" \
    "迷って困っている女の子。少しイライラしているが、最後は苦笑いして諦めている。" \
    "g03_05_困り"

# ============================================================================
# グループ4: キャプションのみ（参照音声なし）
# ============================================================================

echo "===== グループ4: 参照音声なし ====="

run_noref "$TEXT_EMOJI" \
    "少し内気な10代の女の子。緊張しているが、勇気を振り絞って大切なことを伝えようとしている。声は小さめで、途切れ途切れになりがち。" \
    "g04_01_参照なし_キャプションのみ"

# ============================================================================
# グループ5: 矛盾する条件（限界テスト）
# ============================================================================

echo "===== グループ5: 矛盾テスト ====="

# 5-1: 笑いテキスト＋悲しいキャプション
run_cap "あははっ🤭、それ本当に言ってるの？…😮‍💨まぁ、君らしいけどね。" \
    "深く傷つき、今にも泣き出しそうな女の子。声が震えており、悲痛なトーンで弱々しく話す。涙声が混じっている。" \
    "g05_01_矛盾_笑いテキスト悲キャプション"

# 5-2: 悲しいテキスト＋明るいキャプション
run_cap "$TEXT_EMOJI" \
    "明るく元気な10代の女の子。親しい相手に楽しそうに、少し早口で話している。声は高めでハキハキとしている。" \
    "g05_02_矛盾_悲テキスト明キャプション"

echo "===== 全テスト完了 ====="
echo "出力先: ${OUTPUT_DIR}/"
ls -1 "${OUTPUT_DIR}/"*.wav | wc -l | xargs echo "生成ファイル数:"
