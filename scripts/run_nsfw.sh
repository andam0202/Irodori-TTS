set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

OUTPUT_DIR="data/output/mamimi_nsfw"

run() {
    local text="$1"
    local name="$2"
    echo ""
    echo ">>> $name : $text"
    OUTPUT_DIR="$OUTPUT_DIR" bash "${SCRIPT_DIR}/generate_mamimi.sh" "$text" "$name"
}

# VoiceDesign モデル + キャプションで生成（--no-ref、参照音声なし）
run_vd() {
    local text="$1"
    local caption="$2"
    local name="$3"
    local output="${OUTPUT_DIR}/${name}.wav"
    mkdir -p "$OUTPUT_DIR"
    echo ""
    echo ">>> $name (VoiceDesign) : $text"
    echo "    caption: $caption"
    uv run python infer.py \
        --hf-checkpoint Aratako/Irodori-TTS-500M-v2-VoiceDesign \
        --text "$text" \
        --caption "$caption" \
        --no-ref \
        --cfg-scale-caption 3.0 \
        --output-wav "$output"
    echo "    -> $output"
}

