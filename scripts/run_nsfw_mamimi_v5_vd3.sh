# キャプションなし（絵文字のみ）
run() {
    local text="$1" name="$2"
    echo "[generate] ${name}"
    uv run python "${PROJECT_DIR}/infer.py" \
        --checkpoint "$CHECKPOINT" \
        --text "$text" \
        --ref-wav "$REF_WAV" \
        --output-wav "${OUTPUT_DIR}/${name}.wav" \
        --seed "$SEED" \
        2>&1 | tail -1
}

# キャプションあり
